
/**
 * 常量：不会变化的数据.
 * @author RTX 9090
 */
public class Constant {

	public static void main(String[] args) {
		// 1）字面值常量 ,直接是值  int long float double  char boolean String
		System.out.println(10);//int的常量，值就是10
		System.out.println(19223L);//long的常量
		System.out.println(10.1f);//float的常量
		System.out.println(10.11121d);//double的常量
		System.out.println('a');//char的常量,常量的值为'a'
		System.out.println(false);//boolean常量 值为true
		System.out.println("ab");//字符串常量 值是"ab"
		
	}
}
