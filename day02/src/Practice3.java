/**
 * 使用String类型声明姓名name，int类型声明年龄age
 *
 * 如：姓名为：小明 年龄：20
 *
 * String name = “小明”；
 *
 * int age = 20；
 *
 * 打印语句输出： 姓名 ：小明 年龄：20 岁
 */

public class Practice3 {
    public static void main(String[] args) {
        String name = "小明";
        int age = 20;
        System.out.println("姓名：" + name + "\t" + "年龄：" + age + "岁");
    }
}
