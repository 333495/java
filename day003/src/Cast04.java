/**
 * 赋值运算
 *  += -= *= /= %=
 */
public class Cast04 {
    public static void main(String[] args) {
        int a = 53;
        a += 5;//其实就是   a = a + 5;
        System.out.println(a);
        a -= 10;//a = a - 10;
        System.out.println(a);
        a *= 3;//a = a * 3;
        System.out.println(a);
        a /= 2;//a = a / 2;
        System.out.println(a);
        a %= 5;//a = a % 5;
        System.out.println(a);

    }

}
