/**
 * While 循环
 */
public class While01 {
    public static void main(String[] args) {
        //循环
        /*
            while(循环条件){//布尔类型的值 或者 值是布尔类型的表达式
                循环体;//重复做的事情
            }
         */
        int count = 1;
        while (count <= 50){
            count++;
            System.out.println("我爱学java");
        }
    }
}
