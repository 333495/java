public class Add {
    public static void main(String[] args){
        /**
         * 求0-10的和
         */
        int sum = 0;
        for (int i = 0; i <= 10; i++) {
            sum += i;
        }
        System.out.println(sum);

        /**
         * 求0-10的和
         */
        int sum1 = 0;
        for (int i = 0; i <= 10; i++) {
            sum1+=i;
        }
        System.out.println(sum1);
    }
}
